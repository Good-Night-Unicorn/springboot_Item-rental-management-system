					pingyu: '',
					pingjiashijian: '',
					yonghuzhanghao: '',
					yonghuxingming: '',
				},
				xingjipingjiaOptions: [],
				manyiduOptions: [],

				rules: {
					dingdanbianhao: [
					],
					wupinmingcheng: [
					],
					wupinleibie: [
					],
					tupian: [
					],
					xingjipingjia: [
